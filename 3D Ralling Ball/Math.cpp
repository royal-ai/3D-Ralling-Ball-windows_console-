float Math::Vec2_Dot(const VECTOR2& vec1, const VECTOR2& vec2)//返回两个二维向量的点乘
{
	return vec1.x*vec2.x + vec1.y*vec2.y;
};

float Math::Vec2_Length(const VECTOR2& vec)//返回二维向量长度
{
	return sqrtf(vec.x*vec.x + vec.y*vec.y);
}

float Math::Vec3_Dot(const VECTOR3& vec1, const VECTOR3& vec2)//三维向量的点乘
{
	return vec1.x*vec2.x + vec1.y*vec2.y + vec1.z*vec2.z;
};

VECTOR3 Math::Vec3_Cross(const VECTOR3& vec1, const VECTOR3& vec2)//返回向量的叉乘，
//uxv=（Yu*ZvCZu*Yv）*i+（Zu*XvCXu*Zv）*j+（Xu*YvCYu*Xv）*k。
{
	return VECTOR3(
		vec1.y*vec2.z - vec1.z*vec2.y,//这里应该是作者写错了
		vec1.z*vec2.x - vec1.x*vec2.z,
		vec1.x*vec2.y - vec1.y*vec2.x);
};

float Math::Vec3_Length(const VECTOR3& vec)
{
	return sqrtf(vec.x*vec.x + vec.y*vec.y + vec.z*vec.z);
}

VECTOR3 Math::Vec3_Reflect(const VECTOR3 & vec, const VECTOR3 & axis)//以axis为镜面，做反射光线
{
	VECTOR3 In = vec;
	VECTOR3 Out = (-1)*In + 2*(In - Vec3_Dot(In, axis) / In.Length()/axis.Length()*axis);//这里是大小不对吗？？
	//改动，上式添加了axis.Length()
	return Out;
}

float Math::Vec4_Dot(const VECTOR4& vec1, const VECTOR4& vec2)
{
	return vec1.x*vec2.x + vec1.y*vec2.y + vec1.z*vec2.z + vec1.w*vec2.w;
};

float Math::Vec4_Length(const VECTOR4& vec)
{
	return sqrtf(vec.x*vec.x + vec.y*vec.y + vec.z*vec.z + vec.w*vec.w);
}

